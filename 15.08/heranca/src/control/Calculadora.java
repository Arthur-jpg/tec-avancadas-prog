package control;

public class Calculadora {

    /**
     * Metodo que soma dois números reais.
     * @param valor1 xxxx
     * @param valor2 yyyyyy
     * @return um valor double
     * */

    public double somar(final double valor1, final double valor2) {
        return valor1 + valor2;
    }

    /**
     * Metodo que soma dois números reais.
     * @param valor1 xxxx
     * @param valor2 yyyyyy
     * @return um valor double
     * */
    public double subtratir(final double valor1, final double valor2) {
        return valor1 - valor2;
    }

    /**
     * Metodo que soma dois números reais.
     * @param valor1 xxxx
     * @param valor2 yyyyyy
     * @return um valor double
     * */
    public double multiplicar(final double valor1, final double valor2) {
        return valor1 * valor2;
    }

    /**
     * Metodo que soma dois números reais.
     * @param valor1 xxxx
     * @param valor2 yyyyyy
     * @return um valor double
     * */
    public double dividir(final double valor1, final double valor2) {
        return valor1 / valor2;
    }
}
